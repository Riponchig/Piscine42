int	ft_str_is_uppercase(char *str)
{
	int		i;
	int		ret;
	char	c;

	i = 0;
	ret = 1;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!((c >= 'A') && (c <= 'Z')))
		{
			ret = 0;
		}
		i++;
	}
	return (ret);
}
